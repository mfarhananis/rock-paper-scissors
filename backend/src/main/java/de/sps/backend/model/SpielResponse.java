package de.sps.backend.model;

public class SpielResponse {
	
	private int rechnerWahl;
	private int ergebnis;
	
	public SpielResponse() {
		
	}

	public int getRechnerWahl() {
		return rechnerWahl;
	}

	public void setRechnerWahl(int rechnerWahl) {
		this.rechnerWahl = rechnerWahl;
	}

	public int getErgebnis() {
		return ergebnis;
	}

	public void setErgebnis(int ergebnis) {
		this.ergebnis = ergebnis;
	}

}
